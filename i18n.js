function setLang(lang) {
  const currentTranslations = translations[lang] || translations["en"];
  
  document.title = currentTranslations["title"];

  document.querySelectorAll("[data-i18n]").forEach(element => {
    const key = element.getAttribute("data-i18n");
    if (currentTranslations[key]) {
      element.innerHTML = currentTranslations[key];
    }
  });

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  localStorage.setItem('carryover_lang', lang);
}

document.addEventListener("DOMContentLoaded", () => {
  const savedLang = localStorage.getItem('carryover_lang');
  const systemLang = navigator.language.toLowerCase();
  let initialLang = 'en';

  if (savedLang && translations[savedLang]) {
    initialLang = savedLang;
  } else if (systemLang.startsWith('zh')) {
    initialLang = (systemLang === 'zh-tw' || systemLang === 'zh-hk') ? 'zh-Hant' : 'zh-Hans';
  }

  setLang(initialLang);
});
